// toUpperCase() method converts a string to uppercase letters and returns a new string. The original string remains unchanged.

let str = "React JS";
console.log(str.toUpperCase()); // "REACT JS"


// toLowerCase() method converts a string to lowercase letters and returns a new string. The original string remains unchanged.

let str2 = "REACT JS";
console.log(str2.toLowerCase()); // "react js"